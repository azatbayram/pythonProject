# many values to multiple variable
print("-------------------------------")
x , y , z = "orange", "banana", "cherry"
print(x)
print(y)
print(z)

# one value to multiple variables
print("-------------------------------")
x = y = z = 26
print(x)
print(y)
print(z)

# unpack a collection
print("-------------------------------")
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)
