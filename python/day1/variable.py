# Creating variable
print("------------------------------------------")
name = "Azat"
age = 28
married = False
print(name)
print(age)
print(married)

# we can change variable data type after we have been set it
print("------------------------------------------")
x = 5
print(x)
x = "Hello"
print(x)

# casting
print("------------------------------------------")
x = str(3)
y = int(3)
z = float(3)

print(x, y, z)

# get data type of variable
print("------------------------------------------")
x = 5
y = "Azat"

print(type(x))
print(type(y))

# single or double quotes
print("------------------------------------------")
x = "Azat"
y = 'John'
print(x)
print(y)

# case-sensitive
print("------------------------------------------")
a = 5
A = "Sorry"
print(a)
print(A)
